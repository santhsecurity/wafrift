// ──────────────────────────────────────────────
//  Silent block (HTTP 200, but body replaced)
// ──────────────────────────────────────────────

#[tokio::test]
async fn e2e_silent_block_drift_detection() {
    let server = MockServer::start().await;

    // Baseline: benign response
    Mock::given(method("GET"))
        .and(path("/baseline"))
        .respond_with(
            ResponseTemplate::new(200)
                .insert_header("content-type", "text/html")
                .set_body_string(
                    "<html><head><title>My App</title></head>\
                     <body>Search results for: test query</body></html>",
                ),
        )
        .mount(&server)
        .await;

    // Silently blocked: same 200, but body replaced with WAF page
    Mock::given(method("GET"))
        .and(path("/blocked"))
        .respond_with(
            ResponseTemplate::new(200)
                .insert_header("content-type", "text/html")
                .set_body_string(
                    "<html><head><title>Access Denied</title></head>\
                     <body>Your request has been blocked by our web application firewall. \
                     If you believe this is an error, please contact support.</body></html>",
                ),
        )
        .mount(&server)
        .await;

    let client = Client::new();

    let (s1, h1, b1) = fetch(&client, &format!("{}/baseline", server.uri())).await;
    let (s2, h2, b2) = fetch(&client, &format!("{}/blocked", server.uri())).await;

    let fp_baseline = fingerprint(s1, &h1, &b1);
    let fp_blocked = fingerprint(s2, &h2, &b2);

    let drift = compare(&fp_baseline, &fp_blocked);

    assert!(
        drift.score >= 0.4,
        "drift score should be significant: {}",
        drift.score
    );
    assert!(drift.likely_blocked, "should detect as likely blocked");
    assert!(
        drift.changed.contains(&"block_markers_appeared"),
        "should flag block markers as a changed component"
    );
}

