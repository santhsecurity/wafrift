//! End-to-end mock-WAF detection tests.
//!
//! These tests spin up a `wiremock` HTTP server that simulates real WAF
//! response behaviors, then verify the full detect → classify → evasion
//! pipeline against them.  This validates the entire stack without
//! requiring Docker or external infrastructure.
//!
//! # Coverage
//!
//! - Cloudflare challenge page simulation
//! - `ModSecurity` block page simulation
//! - AWS WAF header-only detection
//! - Silent block detection (200 with WAF body markers)
//! - Multiple WAFs behind the same endpoint (ambiguity handling)
//! - Clean (no WAF) response → zero false positives
//! - Response fingerprint drift detection

use reqwest::Client;
use wiremock::matchers::{method, path};
use wiremock::{Mock, MockServer, ResponseTemplate};

use wafrift_detect::{
    response_fingerprint::{compare, fingerprint},
    waf_detect::{self, is_blocked_response},
};

/// Helper: make a GET request and return (status, headers, body).
async fn fetch(client: &Client, url: &str) -> (u16, Vec<(String, String)>, Vec<u8>) {
    let resp = client.get(url).send().await.expect("request failed");
    let status = resp.status().as_u16();
    let headers: Vec<(String, String)> = resp
        .headers()
        .iter()
        .map(|(k, v)| (k.to_string(), v.to_str().unwrap_or("").to_string()))
        .collect();
    let body = resp.bytes().await.expect("body read failed").to_vec();
    (status, headers, body)
}

// ──────────────────────────────────────────────
//  Cloudflare challenge page
// ──────────────────────────────────────────────

#[tokio::test]
async fn e2e_cloudflare_challenge_detected() {
    let server = MockServer::start().await;

    Mock::given(method("GET"))
        .and(path("/"))
        .respond_with(
            ResponseTemplate::new(503)
                .insert_header("server", "cloudflare")
                .insert_header("cf-ray", "abc123-IAD")
                .set_body_string(
                    "<html><head><title>Just a moment...</title></head>\
                     <body>Checking your browser before accessing. \
                     challenge-platform Ray ID: abc123</body></html>",
                ),
        )
        .mount(&server)
        .await;

    let client = Client::new();
    let (status, headers, body) = fetch(&client, &server.uri()).await;

    // Detection
    let wafs = waf_detect::detect(status, &headers, &body);
    assert!(!wafs.is_empty(), "should detect Cloudflare");
    assert_eq!(wafs[0].name, "Cloudflare");
    assert!(
        wafs[0].confidence >= waf_detect::ACTIONABLE_CONFIDENCE_THRESHOLD,
        "confidence should be high: {}",
        wafs[0].confidence
    );

    // Blocking
    assert!(is_blocked_response(status, &body));

    // Fingerprint should show block markers
    let fp = fingerprint(status, &headers, &body);
    assert!(fp.has_block_markers, "should detect WAF block markers");
}

