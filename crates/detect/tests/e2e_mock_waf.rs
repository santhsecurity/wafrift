// ──────────────────────────────────────────────
//  Benign → Blocked fingerprint drift
// ──────────────────────────────────────────────

#[tokio::test]
async fn e2e_fingerprint_identical_responses_zero_drift() {
    let server = MockServer::start().await;

    Mock::given(method("GET"))
        .and(path("/stable"))
        .respond_with(
            ResponseTemplate::new(200)
                .insert_header("content-type", "text/html")
                .set_body_string("<html><body>Stable content</body></html>"),
        )
        .mount(&server)
        .await;

    let client = Client::new();
    let url = format!("{}/stable", server.uri());

    let (s1, h1, b1) = fetch(&client, &url).await;
    let (s2, h2, b2) = fetch(&client, &url).await;

    let fp1 = fingerprint(s1, &h1, &b1);
    let fp2 = fingerprint(s2, &h2, &b2);

    let drift = compare(&fp1, &fp2);
    assert!(
        drift.score < f64::EPSILON,
        "identical responses should have zero drift, got: {}",
        drift.score
    );
    assert!(!drift.likely_blocked);
}

// ──────────────────────────────────────────────
//  Benign → 403 status change drift
// ──────────────────────────────────────────────

#[tokio::test]
async fn e2e_status_change_detected_as_drift() {
    let server = MockServer::start().await;

    Mock::given(method("GET"))
        .and(path("/benign"))
        .respond_with(
            ResponseTemplate::new(200)
                .insert_header("content-type", "text/html")
                .set_body_string("<html><body>OK</body></html>"),
        )
        .mount(&server)
        .await;

    Mock::given(method("GET"))
        .and(path("/attack"))
        .respond_with(
            ResponseTemplate::new(403)
                .insert_header("content-type", "text/html")
                .set_body_string("<html><body>Forbidden. Access Denied.</body></html>"),
        )
        .mount(&server)
        .await;

    let client = Client::new();
    let (s1, h1, b1) = fetch(&client, &format!("{}/benign", server.uri())).await;
    let (s2, h2, b2) = fetch(&client, &format!("{}/attack", server.uri())).await;

    let fp1 = fingerprint(s1, &h1, &b1);
    let fp2 = fingerprint(s2, &h2, &b2);

    let drift = compare(&fp1, &fp2);
    assert!(
        drift.score >= 0.3,
        "status change should register drift: {}",
        drift.score
    );
    assert!(drift.changed.contains(&"status_code"));
    assert!(drift.likely_blocked);
}
